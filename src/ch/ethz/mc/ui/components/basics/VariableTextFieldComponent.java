import lombok.EqualsAndHashCode;

/**
 * Provides the variable text field component
 *
 * @author Andreas Filler
 */
@SuppressWarnings("serial")
@EqualsAndHashCode(callSuper = false)
public class VariableTextFieldComponent extends AbstractCustomComponent {

	/*- VaadinEditorProperties={"grid":"RegularGrid,20","showGrid":true,"snapToGrid":true,"snapToObject":true,"movingGuides":false,"snappingDistance":10} */

	@AutoGenerated
	private HorizontalLayout	mainLayout;
	@AutoGenerated
	private Button				editButton;
	@AutoGenerated
	private GridLayout			variableValueGridLayout;
	@AutoGenerated
	private Label				variableValue;

	/**
	 * The constructor should first build the main layout, set the composition
	 * root and then do any custom initialization.
	 *
	 * The constructor will not be automatically regenerated by the visual
	 * editor.
	 */
	public VariableTextFieldComponent() {
		buildMainLayout();
		setCompositionRoot(mainLayout);

		// manually added
		localize(editButton, AdminMessageStrings.GENERAL__EDIT);

		// add listener
		variableValueGridLayout
				.addLayoutClickListener(new LayoutClickListener() {

					@Override
					public void layoutClick(final LayoutClickEvent event) {
						editButton.click();
					}
				});

		editButton.setDisableOnClick(true);
	}

	/**
	 * Get the button of the component
	 *
	 * @return
	 */
	public Button getButton() {
		return editButton;
	}

	/**
	 * Set the value of the text field
	 *
	 * @param value
	 */
	public void setValue(final String value) {
		if (value == null || value.equals("")) {
			variableValue.setValue(Messages
					.getAdminString(AdminMessageStrings.GENERAL__EMPTY));
		} else {
			variableValue.setValue(value);
		}
	}

	@AutoGenerated
	private HorizontalLayout buildMainLayout() {
		// common part: create layout
		mainLayout = new HorizontalLayout();
		mainLayout.setImmediate(false);
		mainLayout.setWidth("100%");
		mainLayout.setHeight("-1px");
		mainLayout.setMargin(false);
		mainLayout.setSpacing(true);

		// top-level component properties
		setWidth("100.0%");
		setHeight("-1px");

		// variableValueGridLayout
		variableValueGridLayout = buildVariableValueGridLayout();
		mainLayout.addComponent(variableValueGridLayout);
		mainLayout.setExpandRatio(variableValueGridLayout, 1.0f);

		// editButton
		editButton = new Button();
		editButton.setCaption("!!! Edit");
		editButton.setIcon(new ThemeResource("img/edit-icon-small.png"));
		editButton.setImmediate(true);
		editButton.setWidth("100px");
		editButton.setHeight("-1px");
		mainLayout.addComponent(editButton);
		mainLayout.setComponentAlignment(editButton, new Alignment(34));

		return mainLayout;
	}

	@AutoGenerated
	private GridLayout buildVariableValueGridLayout() {
		// common part: create layout
		variableValueGridLayout = new GridLayout();
		variableValueGridLayout.setImmediate(false);
		variableValueGridLayout.setWidth("100.0%");
		variableValueGridLayout.setHeight("-1px");
		variableValueGridLayout.setMargin(false);

		// variableValue
		variableValue = new Label();
		variableValue.setStyleName("v-textfield read-only");
		variableValue.setImmediate(false);
		variableValue.setWidth("100.0%");
		variableValue.setHeight("25px");
		variableValue.setValue(" - NO VALUE SET -");
		variableValueGridLayout.addComponent(variableValue, 0, 0);

		return variableValueGridLayout;
	}

}
