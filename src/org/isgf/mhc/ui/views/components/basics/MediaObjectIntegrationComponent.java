/**
 * Provides the {@link MediaObject} integration component to show, upload and
 * delete media objects
 * 
 * @author Andreas Filler
 */
@SuppressWarnings("serial")
@Data
@EqualsAndHashCode(callSuper = false)
public class MediaObjectIntegrationComponent extends AbstractCustomComponent {

	/*- VaadinEditorProperties={"grid":"RegularGrid,20","showGrid":true,"snapToGrid":true,"snapToObject":true,"movingGuides":false,"snappingDistance":10} */

	@AutoGenerated
	private AbsoluteLayout	mainLayout;
	@AutoGenerated
	private Button			deleteButton;
	@AutoGenerated
	private Upload			uploadComponent;
	@AutoGenerated
	private Embedded		embeddedMediaObject;

	/**
	 * The constructor should first build the main layout, set the
	 * composition root and then do any custom initialization.
	 * 
	 * The constructor will not be automatically regenerated by the
	 * visual editor.
	 */
	public MediaObjectIntegrationComponent() {
		buildMainLayout();
		setCompositionRoot(mainLayout);

		// manually added
	}

	@AutoGenerated
	private AbsoluteLayout buildMainLayout() {
		// common part: create layout
		mainLayout = new AbsoluteLayout();
		mainLayout.setImmediate(false);
		mainLayout.setWidth("100%");
		mainLayout.setHeight("100%");

		// top-level component properties
		setWidth("100.0%");
		setHeight("100.0%");

		// embeddedMediaObject
		embeddedMediaObject = new Embedded();
		embeddedMediaObject.setImmediate(false);
		embeddedMediaObject.setWidth("100.0%");
		embeddedMediaObject.setHeight("100.0%");
		embeddedMediaObject.setSource(new ThemeResource(
				"img/media-object-icon-big.png"));
		embeddedMediaObject.setType(1);
		embeddedMediaObject.setMimeType("image/png");
		mainLayout.addComponent(embeddedMediaObject,
				"top:0.0px;right:0.0px;bottom:30.0px;left:0.0px;");

		// uploadComponent
		uploadComponent = new Upload();
		uploadComponent.setStyleName("upload-component");
		uploadComponent.setImmediate(false);
		uploadComponent.setWidth("100.0%");
		uploadComponent.setHeight("-1px");
		mainLayout.addComponent(uploadComponent,
				"right:105.0px;bottom:0.0px;left:0.0px;");

		// deleteButton
		deleteButton = new Button();
		deleteButton.setCaption("!!! Delete");
		deleteButton.setImmediate(true);
		deleteButton.setWidth("100px");
		deleteButton.setHeight("-1px");
		mainLayout.addComponent(deleteButton, "right:0.0px;bottom:0.0px;");

		return mainLayout;
	}

}
